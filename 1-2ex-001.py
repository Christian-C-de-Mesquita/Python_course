# Estruturas Condicionais:

nota = float(input('Digite qual foi a sua nota na prova de Lógica de programação: '))

if nota >= 90:
    print('Você está indo muito bem, persista e conseguirá atingir seus objetivos!')
elif nota <= 89 and nota >= 70:
    print('Você está quase atingindo a excelência!')
else:
    print('Precisa estudar mais se não quiser ficar retido!')