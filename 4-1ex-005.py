# Conhecendo as tuplas e aprendendo como funcionam:

tupla_teste = (1, 1, 1, 1, 2, 2, 2, 3, 3, 4)

# Número de vezes que um elemento se repetiu
print(tupla_teste.count(1))

# índice aonde apareceu o primeiro elemento da tupla que foi buscado
print(tupla_teste.index(4))

# Leitura de comprimento de uma tupla (quantidade de elementos)
print(len(tupla_teste))